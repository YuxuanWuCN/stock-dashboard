# 根据股票代码确定默认参照指数
def get_market_index(code: str) -> dict:
    if code.startswith("688"):
        return MARKET_INDICES["star50"]
    if code.startswith("3"):
        return MARKET_INDICES["chinext"]
    if code.startswith("0") or code.startswith("2"):
        return MARKET_INDICES["shenzhen"]
    return MARKET_INDICES["shanghai"]

